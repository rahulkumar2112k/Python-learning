# how to take user input in python
#
# In Python, you can use the input() function to take user input from the console.
# The input() function reads a line of text from the console and returns it as a string.
# You can use the input() function to prompt the user for input and store the result in a variable. For example:

# name = input("Enter your name: ")
# print("Hello, " + name)
# In this example, the input() function prompts the user to enter their name, and the result is stored in the variable name.
# The program then prints a greeting message using the user's name.
# 1. Prompting the User: When using the input() function, you can provide a prompt message to instruct the user on what input is expected.
# The prompt message is displayed in the console before the user enters their input. For example:
#
# name = input("Enter your name: ")
# print("Hello, " + name)
# 2. Converting User Input: By default, the input() function returns the user input as a string. 
# If you need to convert the user input to a different data type, such as an integer or float, you can use the int() or float() functions.
# For example:
#
# age = int(input("Enter your age: ")) # Convert user input to an integer
# 3. Handling User Input: When taking user input in Python, it's important to handle cases where the user enters unexpected input. For example, if you expect the user to enter a number but they enter a string, your program may encounter errors. You can use conditional statements and exception handling to validate and handle user input. For example:

